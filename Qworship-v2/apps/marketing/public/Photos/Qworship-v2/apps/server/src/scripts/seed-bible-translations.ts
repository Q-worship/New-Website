/**
 * seed-bible-translations.ts
 *
 * Upserts AMP and MSG Bible text into the existing BibleVerse collection
 * by reading the pre-converted flat JSON files.
 *
 * Each record: { book, chapter, verse, text }
 * Maps to BibleVerse fields: { bookName, chapter, verse, amp/msg }
 *
 * Run:
 *   npx tsx apps/server/src/scripts/seed-bible-translations.ts
 *   OR
 *   ts-node apps/server/src/scripts/seed-bible-translations.ts
 */

